package element;

public class Geometry {

		String height;
		String width;
		String sourceX;
		String sourceY;
		String targetX;
		String targetY;		
		String x;
		String y;
		
		public String getHeight() {
			return height;
		}
		public void setHeight(String height) {
			this.height = height;
		}
		public String getWidth() {
			return width;
		}
		public void setWidth(String width) {
			this.width = width;
		}
		public String getSourceX() {
			return sourceX;
		}
		public void setSourceX(String sourceX) {
			this.sourceX = sourceX;
		}
		public String getSourceY() {
			return sourceY;
		}
		public void setSourceY(String sourceY) {
			this.sourceY = sourceY;
		}
		public String getTargetX() {
			return targetX;
		}
		public void setTargetX(String targetX) {
			this.targetX = targetX;
		}
		public String getTargetY() {
			return targetY;
		}
		public void setTargetY(String targetY) {
			this.targetY = targetY;
		}
		public String getX() {
			return x;
		}
		public void setX(String x) {
			this.x = x;
		}
		public String getY() {
			return y;
		}
		public void setY(String y) {
			this.y = y;
		}
		@Override
		public String toString() {
			return "Geometry [height=" + height + ", width=" + width + ", sourceX=" + sourceX + ", sourceY=" + sourceY
					+ ", targetX=" + targetX + ", targetY=" + targetY + ", x=" + x + ", y=" + y + "]";
		}
		
		
		
		
}


